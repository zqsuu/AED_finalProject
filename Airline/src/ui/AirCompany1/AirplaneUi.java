    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) tblAirplane.getModel();
        model.setRowCount(0);

        for(Airplane air:airlineCompany.getAirplane()){
            Object[] row = new Object[9];
            row[0] = air.getManufacturer();
            row[1] = air.getType();
            if(air.getRoute()==null)
                row[2] = "";
            else
                row[2] = air.getRoute().getRouteName();
            row[3] = air.getFuelConsumption();
            row[4] = air.getPrice();
            row[5] = air.getServiceLife();
            row[6] = air.getFirstCapacity();
            row[7] = air.getBusinessCapacity();
            row[8] = air.getEcoCapacity();
            model.addRow(row);
        }
    }
