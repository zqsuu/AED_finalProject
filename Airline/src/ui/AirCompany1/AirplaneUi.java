    private void cbManuItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbManuItemStateChanged
        // TODO add your handling code here:
        String m = String.valueOf(cbManu.getSelectedItem());
        cbType.removeAllItems();
        for(Manufacturer manu: manufacturerList.getManufacturerList()){
            if(manu.getName().equals(m)){
                for(Airplane air:manu.getAirplane()){
                    cbType.addItem(air.getType());
                }
                break;
            }
        }
        
    }//GEN-LAST:event_cbManuItemStateChanged

    private void cbTypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTypeItemStateChanged
        // TODO add your handling code here:
        String m = String.valueOf(cbManu.getSelectedItem());
        String type = String.valueOf(cbType.getSelectedItem());
        if(m.isEmpty()||type.isEmpty()){
            txtFuel.setText("");
            txtPrice.setText("");
            txtLife.setText("");
            txtFirst.setText("");
            txtBusiness.setText("");
            txtEco.setText("");
        }
        for(Manufacturer manu: manufacturerList.getManufacturerList()){
            if(manu.getName().equals(m)){
                for(Airplane air:manu.getAirplane()){
                    if(air.getType().equals(type)){
                        txtFuel.setText(String.valueOf(air.getFuelConsumption()));
                        txtPrice.setText(String.valueOf(air.getPrice()));
                        txtLife.setText(String.valueOf(air.getServiceLife()));
                        txtFirst.setText(String.valueOf(air.getFirstCapacity()));
                        txtBusiness.setText(String.valueOf(air.getBusinessCapacity()));
                        txtEco.setText(String.valueOf(air.getEcoCapacity()));
                    }
                }
                break;
            }
        }
    }//GEN-LAST:event_cbTypeItemStateChanged


