    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String m = String.valueOf(cbManu.getSelectedItem());
        String type = String.valueOf(cbType.getSelectedItem());
        if(m.isEmpty()||type.isEmpty()){
            return;
        }
        for(Manufacturer manu: manufacturerList.getManufacturerList()){
            if(manu.getName().equals(m)){
                for(Airplane air:manu.getAirplane()){
                    if(air.getType().equals(type)){
                        Airplane a = new Airplane(air,airlineCompany);
                        airlineCompany.addAirplane(a);
                        a.updateDB();
                        break;
                    }
                }
                break;
            }
        }
        JOptionPane.showMessageDialog(this, "Airplane bought successfully!");
    }//GEN-LAST:event_btnSaveActionPerformed

