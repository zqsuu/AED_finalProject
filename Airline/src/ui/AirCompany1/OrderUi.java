    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) tblReservation.getModel();
        tblModel.setRowCount(0);
        
        String content = txtSearch.getText();
        String state = String.valueOf(cbState.getSelectedItem());
        boolean search = false;
        
        
        for(FlightReservation fr:airlineCompany.getOrder()){
            if(state.equals("all")||(!state.equals("all")&&fr.getState().equals(state))){
                if(fr.getId().equals(content))
                    search = true;
                if(fr.getPassenger().equals(content))
                    search = true;
                if(String.valueOf(fr.getPrice()).equals(content))
                    search = true;
                if(fr.getClass().equals(content))
                    search = true;
                if(fr.getRoute().getRouteName().equals(content))
                    search = true;
                if(search){
                    Object[] row = new Object[7];
                    row[0] = fr.getId();
                    row[1] = fr.getPassenger().getName();
                    row[2] = fr.getRoute().getRouteName();
                    row[3] = fr.getState();
                    row[4] = fr.getDate();
                    row[5] = fr.getClass();
                    row[6] = fr.getPrice();
                    tblModel.addRow(row);
                    search = false;
                }
                
            }
        }
        
        txtArrival.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

