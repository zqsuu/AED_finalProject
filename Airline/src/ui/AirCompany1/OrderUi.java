    private void tblReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblReservationMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)tblReservation.getModel();
        
        int index = tblReservation.getSelectedRow();
        
        if(index<0){
            txtArrival.setText("");
            txtDeparture.setText("");
            return;
        }
        
        String name = String.valueOf(tblModel.getValueAt(index, 1));
        for(Route r:airlineCompany.getRoute()){
            if(r.getRouteName().equals(name)){
                txtDeparture.setText(r.getDepartureAirport().getName());
                txtArrival.setText(r.getFallAirport().getName());
                break;
            }
        }
        
        
        
    }//GEN-LAST:event_tblReservationMouseClicked

