    public void populateTable(){
        DefaultTableModel tblModel = (DefaultTableModel) tblReservation.getModel();
        tblModel.setRowCount(0);
        String state = String.valueOf(cbState.getSelectedItem());
        
        for(FlightReservation fr:airlineCompany.getOrder()){
            if(state.equals("all")){
                Object[] row = new Object[7];
                row[0] = fr.getId();
                row[1] = fr.getPassenger().getName();
                row[2] = fr.getRoute().getRouteName();
                row[3] = fr.getState();
                row[4] = fr.getDate();
                row[5] = fr.getClass();
                row[6] = fr.getPrice();
                tblModel.addRow(row);
            }
            else if(fr.getState().equals(state)){
                Object[] row = new Object[7];
                row[0] = fr.getId();
                row[1] = fr.getPassenger().getName();
                row[2] = fr.getRoute().getRouteName();
                row[3] = fr.getState();
                row[4] = fr.getDate();
                row[5] = fr.getClass();
                row[6] = fr.getPrice();
                tblModel.addRow(row);
            }
        }
    }

