    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblReservation.getModel();
        model.setRowCount(0);
        String state = String.valueOf(cbState.getSelectedItem());
        
        try{
            ResultSet rs = st.executeQuery("Select * from airlinedb1.flightreservation");
            while(rs.next()){
                
                    if(state.equals("all")){
                        Object[] row = new Object[7];
                        row[0] = rs.getString("reservationid");
                        row[1] = rs.getString("passengername");
                        row[2] = rs.getString("route");
                        row[3] = rs.getString("state");
                        row[4] = rs.getString("date");
                        row[5] = rs.getString("class");
                        row[6] = rs.getInt("price");
                        model.addRow(row);  
                    }
                    else if(rs.getString("state").equals(state)){
                        Object[] row = new Object[7];
                        row[0] = rs.getString("reservationid");
                        row[1] = rs.getString("passengername");
                        row[2] = rs.getString("route");
                        row[3] = rs.getString("state");
                        row[4] = rs.getString("date");
                        row[5] = rs.getString("class");
                        row[6] = rs.getInt("price");
                        model.addRow(row);  
                    }
                
                
            }
        }catch(Exception e){
            e.printStackTrace();
 
        }      
        
        
    }

