    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)tblReservation.getModel();
        
        int index = tblReservation.getSelectedRow();
        
        if(index<0){
            JOptionPane.showMessageDialog(this, "Please select a row");    
            return;
        }
        
        String id = String.valueOf(tblModel.getValueAt(index, 0));
        if(!String.valueOf(tblModel.getValueAt(index, 3)).equals("purchased")){
            JOptionPane.showMessageDialog(this, "It can be canceled just when order's state is purchased");    
            return;
        }
        
        try{
            String query = "UPDATE airlinedb1.flightreservation SET state = '" + "canceled" +  "' WHERE reservationid = '" + id+"'";
            st.executeUpdate(query);
        }catch(Exception e){
            e.printStackTrace();
 
        }      
        populateTable();
        
    }//GEN-LAST:event_jButton4ActionPerformed

