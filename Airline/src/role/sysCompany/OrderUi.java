    private void tblReservationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblReservationMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)tblReservation.getModel();
        
        int index = tblReservation.getSelectedRow();
        
        if(index<0){
            txtArrival.setText("");
            txtDeparture.setText("");
            return;
        }
        
        String name = String.valueOf(tblModel.getValueAt(index, 2));
        
        try{
            ResultSet rs = st.executeQuery("Select * from airlinedb1.route");
            while(rs.next()){
                if(rs.getString("routename").equals(name)){
                    txtArrival.setText(rs.getString("departureairport"));
                    txtDeparture.setText(rs.getString("fallairport"));
                    break;
                }
            }
            
        }catch(Exception e){
            e.printStackTrace();
 
        } 

        
    }//GEN-LAST:event_tblReservationMouseClicked

